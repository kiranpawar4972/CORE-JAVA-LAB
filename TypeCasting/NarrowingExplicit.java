package com.typecasting;

public class NarrowingExplicit 
{

	public static void main(String[] args) 
	{
		double v1=44.35576879;
		System.out.println(v1);
		
		float v2=(float)v1;
		System.out.println(v2);
		
		long v3=(long)v1;
		System.out.println(v3);
		
		int v4=97;
		char v5=(char)v4;
		System.out.println(v5);

	}

}
