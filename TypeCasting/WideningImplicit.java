package com.typecasting;

public class WideningImplicit 
{

	public static void main(String[] args) 
	{
		//widening/implicit type casting
		
		byte age=18;
		int data=age;
		System.out.println(data);
		
		char v1='A';
		int v2=v1;
		System.out.println(v2);
		
		int v3=55;
		float v4=v3;
		System.out.println(v4);

	}

}
